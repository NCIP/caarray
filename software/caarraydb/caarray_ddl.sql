CREATE TABLE TERM
(
  ID                          NUMBER            NOT NULL,
  DESCRIPTION                 VARCHAR2(1000),
  VALUE                       VARCHAR2(1000),
  CATEGORY_ID                 NUMBER            NOT NULL,
  ACCESSION_ID                NUMBER,
  SOURCE_ID                   NUMBER
)
NOLOGGING 
NOCACHE
NOPARALLEL;

CREATE TABLE CATEGORY
(
  ID                          NUMBER            NOT NULL,
  NAME                        VARCHAR2(1000),
  PARENT_ID                   NUMBER            NOT NULL
)
NOLOGGING 
NOCACHE
NOPARALLEL;

CREATE TABLE CATEGORYCHILDREN
(
  CATEGORY_ID           NUMBER                  NOT NULL,
  CHILD_CATEGORY_ID     NUMBER                  NOT NULL
)
NOLOGGING 
NOCACHE
NOPARALLEL;

CREATE TABLE ACCESSION
(
  ID                          NUMBER            NOT NULL,
  URL                         VARCHAR2(1000),
  VALUE                       VARCHAR2(1000),
  SOURCE_ID                   NUMBER            NOT NULL
)
NOLOGGING 
NOCACHE
NOPARALLEL;

CREATE TABLE SOURCE
(
  ID                          NUMBER            NOT NULL,
  NAME                        VARCHAR2(1000),
  URL                         VARCHAR2(1000),
  VERSION                     VARCHAR2(1000)
)
NOLOGGING 
NOCACHE
NOPARALLEL;

