	CHROMOSOME_ID NUMERIC(10) NOT NULL,
	CHROMOSOME_NUMBER VARCHAR2(50) NOT NULL,
	TAXON_ID NUMERIC(10) NOT NULL,
	PRIMARY KEY(CHROMOSOME_ID)
) 
;

CREATE TABLE CLONE ( 
	CLONE_ID NUMERIC(10) NOT NULL,
	INSERT_SIZE NUMERIC(10),
	CLONE_NAME VARCHAR2(50),
	VERSION VARCHAR2(100),
	ACCESSION_NUMBER VARCHAR2(15),
	CLONE_STRAIN VARCHAR2(50),
	LIBRARY_ID NUMERIC(10),
	VERIFIED CHAR(1) DEFAULT 0 NOT NULL,
	PRIMARY KEY(CLONE_ID)
) 
;

CREATE TABLE GENE ( 
	GENE_ID NUMERIC(10) NOT NULL,
	CLUSTER_ID NUMERIC(10),
	GENE_TITLE VARCHAR2(50),
	LOCUS_LINK_ID VARCHAR2(50),
	GENE_SYMBOL VARCHAR2(50),
	OMIM_ID VARCHAR2(50),
	LOCUS_LINK_SUMMARY VARCHAR2(4000),
	CHROMOSOME_ID NUMERIC(10),
	TAXON_ID NUMERIC(10),
	PRIMARY KEY(GENE_ID)
) 
;

