CREATE TABLE CHROMOSOME ( 
	CHROMOSOME_ID NUMERIC(10) NOT NULL,
	CHROMOSOME_NUMBER VARCHAR(50) NOT NULL,
	TAXON_ID NUMERIC(10) NOT NULL,
	PRIMARY KEY(CHROMOSOME_ID)
) 
;

CREATE TABLE CLONE ( 
	CLONE_ID NUMERIC(10) NOT NULL,
	INSERT_SIZE NUMERIC(10),
	CLONE_NAME VARCHAR(50),
	VERSION VARCHAR(100),
	ACCESSION_NUMBER VARCHAR(15),
	CLONE_STRAIN VARCHAR(50),
	LIBRARY_ID NUMERIC(10),
	VERIFIED BOOL DEFAULT 0 NOT NULL,
	PRIMARY KEY(CLONE_ID)
) 
;

CREATE TABLE GENE ( 
	GENE_ID NUMERIC(10) NOT NULL,
	CLUSTER_ID NUMERIC(10),
	GENE_TITLE VARCHAR(50),
	LOCUS_LINK_ID VARCHAR(50),
	GENE_SYMBOL VARCHAR(50),
	OMIM_ID VARCHAR(50),
	LOCUS_LINK_SUMMARY TEXT,
	CHROMOSOME_ID NUMERIC(10),
	TAXON_ID NUMERIC(10),
	PRIMARY KEY(GENE_ID)
) 
;

CREATE TABLE GENE_SEQUENCE ( 
	GENE_ID NUMERIC(10) NOT NULL,
	SEQUENCE_ID NUMERIC(10) NOT NULL,
	PRIMARY KEY(GENE_ID, SEQUENCE_ID)
) 
;

CREATE TABLE GENE_TARGET ( 
	GENE_ID NUMERIC(10) NOT NULL,
	TARGET_ID NUMERIC(10) NOT NULL,
	PRIMARY KEY(GENE_ID, TARGET_ID)
) 
;

CREATE TABLE LIBRARY ( 
	LIBRARY_ID NUMERIC(10) NOT NULL,
	LIBRARY_TYPE VARCHAR(50),
	LIBRARY_NAME VARCHAR(50),
	DESCRIPTION VARCHAR(50),
	KEYWORD VARCHAR(50),
	R_SITE_1 VARCHAR(50),
	R_SITE_2 VARCHAR(50),
	SEQUENCES_TO_DATE NUMERIC(10),
	CLONES_TO_DATE NUMERIC(10),
	UNIGENE_ID NUMERIC(10),
	CREATE_DATE DATE,
	LAB_HOST VARCHAR(50),
	PRIMARY KEY(LIBRARY_ID)
) 
;

CREATE TABLE LIBRARY_GENE ( 
	GENE_ID NUMERIC(10) NOT NULL,
	LIBRARY_ID NUMERIC(10) NOT NULL,
	PRIMARY KEY(GENE_ID, LIBRARY_ID)
) 
;

CREATE TABLE MONKEY ( 
	MONKEY_ID NUMERIC(10) NOT NULL,
	NAME VARCHAR(50),
	TARGET_ID NUMERIC(10),
	PRIMARY KEY(MONKEY_ID)
) 
;

CREATE TABLE SEQUENCE ( 
	SEQUENCE_ID NUMERIC(10) NOT NULL,
	ACCESSION_NUMBER VARCHAR(50),
	IS_REF_SEQUENCE BOOL DEFAULT 0 NOT NULL,
	DESCRIPTION VARCHAR(4),
	ASCII_STRING TEXT,
	LENGTH NUMERIC(10),
	SEQUENCE_TYPE NUMERIC(10),
	VERSION VARCHAR(10),
	CLONE_ID NUMERIC(10),
	PRIMARY KEY(SEQUENCE_ID)
) 
;

CREATE TABLE TARGET ( 
	TARGET_ID NUMERIC(10) NOT NULL,
	TARGET_NAME VARCHAR(50),
	TARGET_TYPE CHAR(50),
	PRIMARY KEY(TARGET_ID)
) 
;

CREATE TABLE TAXON ( 
	TAXON_ID NUMERIC(10) NOT NULL,
	SCIENTIFIC_NAME VARCHAR(50),
	STRAIN_OR_ETHNICITY VARCHAR(50),
	COMMON_NAME VARCHAR(50),
	PREFERRED BOOL DEFAULT 0 NOT NULL,
	ABBREVIATION VARCHAR(50),
	PRIMARY KEY(TAXON_ID)
) 
;



